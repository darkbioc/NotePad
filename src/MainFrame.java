    private void jMenuOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuOpenActionPerformed
       int returnVal = jFileChooser.showOpenDialog(this);
       if (returnVal == JFileChooser.APPROVE_OPTION) 
       {
        fileToSave = jFileChooser.getSelectedFile();
        try 
        {
          // What to do with the file, e.g. display it in a TextArea
          jTextArea.read( new FileReader( fileToSave.getAbsolutePath() ), null );
          saved=true;
        } 
        catch (IOException ex) 
        {
          System.out.println("problem accessing file"+fileToSave.getAbsolutePath());
        }
       }
    }//GEN-LAST:event_jMenuOpenActionPerformed

    private void jMenuSaveAsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuSaveAsActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Specify a file to save");   
        int userSelection = fileChooser.showSaveDialog(this);
        if (userSelection == JFileChooser.APPROVE_OPTION) 
        {
        canceled=false;
        fileToSave = fileChooser.getSelectedFile();
        BufferedWriter bw = null;
            try {
                //fileToSave.createNewFile();
                bw = new BufferedWriter(new FileWriter(fileToSave));
                bw.write(jTextArea.getText());
                saved=true;
                //System.out.println("Save as file: " + fileToSave.getAbsolutePath());
            } catch (IOException ex) {
                ex.printStackTrace();
            }
            finally
            {
                if(bw != null)
                {
                    try {
                        bw.close();
                    } catch (IOException ex) {
                        ex.printStackTrace();
                    }
                }
            }
        }
        else
        {
            canceled=true;
        }
    }//GEN-LAST:event_jMenuSaveAsActionPerformed

